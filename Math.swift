import Foundation

struct Math {
    
    static func addTwoNumbers(addend1: Double, addend2: Double) -> Double {
        return addend1  + addend2
    }
    
    static func divideNumbers(dividend: Double, divisor: Double) -> Double{
        return dividend/divisor
    }
}
